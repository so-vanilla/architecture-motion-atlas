function makeArchitecture(blueprint: Blueprint): Architecture {
  const topology = topologyFor(blueprint);
  const firstQuality = qualityNames[blueprint.quality[0]];
  const secondQuality = qualityNames[blueprint.quality[1]];
  const thirdQuality = qualityNames[blueprint.quality[2]];
  const fourthQuality = qualityNames[blueprint.quality[3]];
  return {
    id: blueprint.id,
    category: blueprint.category,
    title: blueprint.title,
    summary: blueprint.summary,
    essence: blueprint.essence,
    primaryQuestion: blueprint.primaryQuestion,
    notThis: blueprint.notThis,
    qualityFocus: qualityFocus(blueprint.quality),
    decisionForces: decisionForces(blueprint),
    diagram: makeDiagram(blueprint),
    benefits: [
      `${topology.support} を明示したことで、${blueprint.strong}`,
      `${topology.entry}、${topology.core}、${topology.support} の境界が見えるため、${firstQuality} と ${secondQuality} を優先する判断を説明しやすい。`,
    ],
    tradeoffs: [
      `${topology.risk} が弱点として表に出るため、${blueprint.weak}`,
      `${topology.risk} を抑えるには、${thirdQuality} や ${fourthQuality} の負担を観測、テスト、運用ルールとして引き受ける必要がある。`,
    ],
    appliesWhen: [
      `${topology.support} を独立して増やす、差し替える、または検証する判断が ${blueprint.primaryQuestion} と一致する場合。`,
      `${blueprint.boundary} を ${topology.entry}、${topology.core}、${topology.support} の責務として所有できる場合。`,
    ],
    weakWhen: [
      `${topology.data} と ${topology.risk} の関係を、要求増加、変更、障害時に説明できない場合。`,
      `${topology.risk} を検知・説明する観測や契約管理を継続できない場合。`,
    ],
    relatedArchitectures: blueprint.related,
    questions: commonQuestions(blueprint),
  };
}

export const systemArchitectures = systemBlueprints.map(makeArchitecture);
export const softwareArchitectures = softwareBlueprints.map(makeArchitecture);
export const architectures = [...systemArchitectures, ...softwareArchitectures];

export function findArchitecture(id: string): Architecture | undefined {
  return architectures.find((architecture) => architecture.id === id);
}

export function architecturesByCategory(category: ArchitectureCategory): Architecture[] {
  return architectures.filter((architecture) => architecture.category === category);
}

export const architectureIds = {
  system: [...systemIds],
  software: [...softwareIds],
};
